// for of
// Оператор for...of выполняет цикл обхода итерируемых объектов 
// (включая Array, Map (en-US), Set, объект аргументов и подобных), 
// вызывая на каждом шаге итерации операторы для каждого значения 
// из различных свойств объекта.

const num = "090909"
for (let i of num)
console.log('i', i) // i-0 i-9 i-0 i-9 i-0 i-9

//          for...in
// Цикл for...in проходит через перечисляемые свойства объекта. 
// Он пройдёт по каждому отдельному элементу.
const num1 = "090909"
for (let i in num1)
console.log('i', i)  // i-0 i-1 i-2 i-3 i-4 i-5