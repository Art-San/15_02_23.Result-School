// -------------  do while  ----------
// Выражение do...while создаёт цикл, который выполняет указанное выражение до тех пор, 
// пока условие не станет ложным. Условие проверяется после выполнения выражения, 
// то есть выражение выполнится как минимум один раз.

// do {
// } while (условие)

// let i = 0;
// do {
//     console.log('i', i);
//     i += 1;
// } while (i <= 10);

let result = '';
let i = 0;

do {
  i = i + 1;
  result = result + i;
} while (i < 5);

console.log(result);
// expected result: "12345"



